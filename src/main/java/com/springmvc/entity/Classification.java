package com.springmvc.entity;

public class Classification {
    private Integer classificationinfoId;

    private Integer classificationinfoCode;

    private String classificationinfoName;

    private String classificationinfoDescription;

    public Integer getClassificationinfoId() {
        return classificationinfoId;
    }

    public void setClassificationinfoId(Integer classificationinfoId) {
        this.classificationinfoId = classificationinfoId;
    }

    public Integer getClassificationinfoCode() {
        return classificationinfoCode;
    }

    public void setClassificationinfoCode(Integer classificationinfoCode) {
        this.classificationinfoCode = classificationinfoCode;
    }

    public String getClassificationinfoName() {
        return classificationinfoName;
    }

    public void setClassificationinfoName(String classificationinfoName) {
        this.classificationinfoName = classificationinfoName;
    }

    public String getClassificationinfoDescription() {
        return classificationinfoDescription;
    }

    public void setClassificationinfoDescription(String classificationinfoDescription) {
        this.classificationinfoDescription = classificationinfoDescription;
    }
}